#include <stdio.h>

int main() {
  int x = 5;
  int y = 2;
  printf("%d \n", x + y); // addition operator
  printf("%d \n", x - y); // Subtraction operator
  printf("%f \n", x /y);  // Division operator
  printf("%d \n", x * y); // Multiplication operator
  printf("%d \n", x % y); // Modulus operator
  return 0;
}