#include <stdio.h>

int main() {
  int x = 5;
  int y = 2;
  printf("%d \n", ++x); // increment operator
  printf("%d \n", --y); // decrement operator

  printf("%d \n", x++); // increment operator
  printf("%d \n", y--); // decrement operator

  printf("%d \n", x); // increment operator
  printf("%d \n", y); // decrement operator

  return 0;
}
